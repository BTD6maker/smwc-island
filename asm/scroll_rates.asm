	dw $0000,$0040,$0133,$0040	; 04 - 07
	dw $FFFF,$0020,$0020,$0000	; 08 - 0B
	dw $0020,$0000,$0040,$0000	; 0C - 0F
	dw $0100,$0020,$0040,$0060	; 10 - 13
	dw $0000,$0008			; 14 - 15
	dw !bg3_16_v
	dw !bg3_17_v
	dw $0100,$0100,$0100,$0100	; 18 - 1B
	dw $0100,$0100,$0100,$0100	; 1C - 1F

