; $04FE43 is the code that does stuff with this

!bg2_16_h = $0040
!bg2_16_v = $0018
!bg3_16_h = $0100
!bg3_16_v = $FFFF

!bg2_17_h = $0080
!bg2_17_v = $0010
!bg3_17_h = $0100
!bg3_17_v = $0100

org $04FB6E

BG2_horizontal_rates:
	dw $0040,$0080,$0100,$0080	; 00 - 03
	dw $0080,$0080,$0080,$0080	; 04 - 07
	dw $0040,$00C0,$0080,$00C0	; 08 - 0B
	dw $0000,$0080,$0000,$0000	; 0C - 0F
	dw $0080,$0080,$0080,$0080	; 10 - 13
	dw $0040,$0080			; 14 - 15
	dw !bg2_16_h
	dw !bg2_17_h
	dw $0100,$0100,$0100,$0100	; 18 - 1B
	dw $0100,$0100,$0100,$0100	; 1C - 1F

BG2_vertical_rates:
	dw $0040,$FFFF,$FFFF,$0040	; 00 - 03
	dw $0040,$0040,$0040,$0040	; 04 - 07
	dw $FFFF,$0060,$0040,$FFFF	; 08 - 0B
	dw $0000,$FFFF,$0000,$0000	; 0C - 0F
	dw $0100,$0040,$0040,$0040	; 10 - 13
	dw $0040,$0040			; 14 - 15
	dw !bg2_16_v
	dw !bg2_17_v
	dw $0100,$0100,$0100,$0100	; 18 - 1B
	dw $0100,$0100,$0100,$0100	; 1C - 1F

